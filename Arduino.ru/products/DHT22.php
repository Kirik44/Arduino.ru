<h4 style="text-align: center;">Датчик температуры и влажности DHT22</h4>
<p>DHT-22 (также называемый AM2302)  представляет собой датчик влажности и температуры с цифровым выходом, для измерения используется емкостной датчик влажности и термистор, все показания передаются по цифровой информационной шине.</p>
<h5 class="h5_content">Технические параметры</h5>
<ul>
    <li>Модель: DHT22</li>
    <li>Напряжение питания: 3.3 В … 5 В</li>
    <li>Выходной сигнал: цифровой</li>
    <li>Чувствительный элемент: полимерный конденсатор</li>
    <li>Диапазон измерения влажности: 0 … 100%, погрешность ±2%</li>
    <li>Диапазон измерения температуры:  -40°С … +80°С, погрешность  ±0.5°С</li>
    <li>Задержка: 2с</li>
    <li>Габариты: 15.1 мм x 25.1 мм x 7.7 мм</li>
</ul>
<h5 class="h5_content">Общие сведения</h5>
<p>Датчики DHT22 состоит из чувствительного емкостного датчика и NTC-термистора, а так же 8-ми битном чипе, который преобразует аналоговый сигнал с датчиков, в цифровой на выходе. При производстве, компоненты входящие в модуль DHT22 имеют разные параметры и чтобы показания были реальными, производитель калибрует каждый датчик DHT22 в калибровочной камере, а поправочный коэффициент сохраняется в памяти и вызывается при считывании данных. Преимущество данных датчиков, это небольшие размеры, низкое энергопотребление. К недостаткам можно отнести задержку передачи показаний в 2 секунды.</p>
