<h5 class="h5_content">Подключение датчика DHT22 к Arduino</h5>
<p>В примере все подключение буду осуществлять через макетную плату и провода DuPont, собираем все по схеме</p>
<img src="img/products/dht22/5.jpg" style="width: 40rem">
<p>Как видите схема не сложная, информация будет отображаться в <strong>«Мониторинге порта»</strong>. Далее для работы скетча необходима дополнительные библиотеки <strong>DHT.h</strong> и <strong>Adafruit_Sensor.h</strong>, скачиваем и установить их, а то программа выдаст ошибку. Запускаем среду разработки IDE Arduino и смотрим что получилось.</p>
